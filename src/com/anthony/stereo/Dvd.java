package com.anthony.stereo;

public class Dvd {

    String filmName;
    String producer;

    public Dvd(String filmName, String producer) {
        this.filmName = filmName;
        this.producer = producer;
    }

    public String getFilmName() {
        return filmName;
    }

    public String getProducer() {
        return producer;
    }
}
